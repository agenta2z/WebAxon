"""Sync .hbs templates back to Python string constants in AgentFoundation.

This utility converts Handlebars templates (.hbs) back to Python .format()-style
string constants, enabling .hbs files to serve as the authoritative source of truth
for prompt content while maintaining backward compatibility with consumers that use
Python .format() substitution.

Usage:
    python sync_templates.py [--dry-run] [--verify]

    --dry-run   Show what would change without writing files
    --verify    Run parity test after sync to validate round-trip fidelity

Conversion rules (.hbs → Python .format()):
    {{{variable}}}  →  {variable}     (Handlebars unescaped → Python format var)
    {               →  {{              (literal brace needs escaping in .format())
    }               →  }}              (literal brace needs escaping in .format())

Note: Structuring/Classification templates use domain_taxonomy which is dynamically
generated by format_taxonomy_for_prompt(). The sync utility preserves the raw template
constants; the wrapper functions (get_structuring_prompt, get_classification_prompt)
remain unchanged.
"""

import re
import sys
import argparse
from pathlib import Path
from typing import Dict, List, NamedTuple, Optional


class TemplateMapping(NamedTuple):
    """Maps an .hbs template to its Python target."""
    hbs_path: str                # Relative path from this script's directory
    python_file: str             # Relative path to Python file from AgentFoundation prompts dir
    constant_name: str           # Name of the Python constant to update
    skip_sync: bool = False      # If True, skip this template (e.g., no Python counterpart)
    format_specs: Dict[str, str] = {}  # Variable → Python format spec (e.g., {"similarity": ":.3f"})


# Paths
