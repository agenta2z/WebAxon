def hbs_to_python_format(hbs_content: str, format_specs: Optional[Dict[str, str]] = None) -> str:
    """Convert Handlebars template content to Python .format()-style string.

    Conversion steps:
    1. Strip {{!-- ... --}} comment block at the top
    2. Convert {{{variable}}} → {variable} (or {variable:.3f} if format_specs provided)
    3. Escape remaining literal { → {{ and } → }} for .format() compatibility

    The order matters: we must handle {{{var}}} before escaping literal braces,
    otherwise the conversion would double-escape.

    Args:
        hbs_content: Raw Handlebars template content.
        format_specs: Optional dict mapping variable names to Python format specs
                      (e.g., {"similarity": ":.3f"}). These are appended to the
                      variable in the Python .format() output since Handlebars
                      has no equivalent of Python format specs.
    """
    format_specs = format_specs or {}

    # Step 1: Strip leading Handlebars comment
    content = re.sub(r"^\s*\{\{!--.*?--\}\}\s*", "", hbs_content, count=1, flags=re.DOTALL)

    # Step 2: Extract {{{variable}}} placeholders and replace with temporary markers
    # We use a marker that won't appear in normal text
    placeholders: Dict[str, str] = {}
    marker_idx = 0

    def replace_placeholder(match):
        nonlocal marker_idx
        var_name = match.group(1)
        marker = f"\x00PLACEHOLDER_{marker_idx}\x00"
        placeholders[marker] = var_name
        marker_idx += 1
        return marker

    content = re.sub(r"\{\{\{(\w+)\}\}\}", replace_placeholder, content)

    # Step 3: Escape literal braces for Python .format()
    content = content.replace("{", "{{").replace("}", "}}")

    # Step 4: Restore placeholders as Python .format() variables
    # Apply format specs where specified (e.g., similarity → similarity:.3f)
    for marker, var_name in placeholders.items():
        spec = format_specs.get(var_name, "")
        content = content.replace(marker, f"{{{var_name}{spec}}}")

    return content


def read_python_file(python_path: Path) -> str:
    """Read a Python file's content."""
    return python_path.read_text(encoding="utf-8")


def update_constant_in_python(
    python_content: str,
    constant_name: str,
    new_value: str,
) -> Optional[str]:
    """Replace a triple-quoted string constant in Python source code.

    Finds the constant assignment (e.g., PROMPT = \"\"\"...\"\"\") and replaces
    the string value while preserving surrounding code.

    Returns updated content, or None if constant not found.
    """
    # Match: CONSTANT_NAME = """..."""  (triple-quoted string)
    pattern = re.compile(
        rf'^({re.escape(constant_name)}\s*=\s*)(""".*?""")',
        re.MULTILINE | re.DOTALL,
    )

    match = pattern.search(python_content)
    if not match:
        return None

    prefix = match.group(1)  # e.g., 'DEDUP_LLM_JUDGE_PROMPT = '
    new_assignment = f'{prefix}"""{new_value}"""'
    return python_content[:match.start()] + new_assignment + python_content[match.end():]


def sync_template(mapping: TemplateMapping, dry_run: bool = False) -> bool:
    """Sync a single .hbs template to its Python constant.

    Returns True if sync succeeded (or no change needed), False on error.
    """
    if mapping.skip_sync:
        print(f"  SKIP {mapping.hbs_path} (manual sync only)")
        return True

    hbs_path = SCRIPT_DIR / mapping.hbs_path
    python_path = AGENT_FOUNDATION_PROMPTS / mapping.python_file

    if not hbs_path.exists():
        print(f"  ERROR {mapping.hbs_path}: .hbs file not found")
        return False

    if not python_path.exists():
        print(f"  ERROR {mapping.python_file}: Python file not found")
        return False

    # Read and convert (apply format specs for variables like similarity:.3f)
    hbs_content = hbs_path.read_text(encoding="utf-8")
    converted = hbs_to_python_format(hbs_content, format_specs=mapping.format_specs)

    # Read current Python file
    python_content = read_python_file(python_path)

    # Update constant
    updated = update_constant_in_python(python_content, mapping.constant_name, converted)
    if updated is None:
        print(f"  ERROR {mapping.constant_name}: constant not found in {mapping.python_file}")
        return False

    if updated == python_content:
        print(f"  OK {mapping.constant_name} (no change)")
        return True

    if dry_run:
        print(f"  WOULD UPDATE {mapping.constant_name} in {mapping.python_file}")
        return True

    python_path.write_text(updated, encoding="utf-8")
    print(f"  UPDATED {mapping.constant_name} in {mapping.python_file}")
    return True


def main():
    parser = argparse.ArgumentParser(description="Sync .hbs templates to Python constants")
    parser.add_argument("--dry-run", action="store_true", help="Show changes without writing")
    parser.add_argument("--verify", action="store_true", help="Run parity test after sync")
    args = parser.parse_args()

    print(f"Syncing .hbs templates → Python constants")
    print(f"  Source: {SCRIPT_DIR}")
    print(f"  Target: {AGENT_FOUNDATION_PROMPTS}")
    print()

    if not AGENT_FOUNDATION_PROMPTS.exists():
        print(f"ERROR: Target directory not found: {AGENT_FOUNDATION_PROMPTS}")
        sys.exit(1)

    success = True
    for mapping in MAPPINGS:
        if not sync_template(mapping, dry_run=args.dry_run):
            success = False

    print()
    if args.dry_run:
        print("Dry run complete. No files were modified.")
    elif success:
        print("Sync complete.")
    else:
        print("Sync completed with errors.")

    if args.verify and not args.dry_run:
        print("\nRunning parity test...")
        from test_template_parity import run_parity_tests
        if not run_parity_tests():
            print("PARITY TEST FAILED — sync may have introduced differences")
            sys.exit(1)

    sys.exit(0 if success else 1)


if __name__ == "__main__":
    main()
